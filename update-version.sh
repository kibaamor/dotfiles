require_command() {
  if ! command -v "$1" >/dev/null 2>&1; then
    echo "error: required command not found: $1" >&2
    exit 1
  fi
}

load_existing_versions() {
  local key
  local line
  local section=""
  local value

  [[ -f "$VERSIONS_FILE" ]] || return

  # The files are generated by this script, so a lightweight parser is enough
  # and avoids adding a YAML dependency just to read simple top-level maps.
  while IFS= read -r line; do
    case "$line" in
    "versions:")
      section="versions"
      ;;
    "commits:")
      section="commits"
      ;;
    "  "*": "*)
      key="${line%%:*}"
      key="${key#"${key%%[![:space:]]*}"}"
      value="${line#*: }"
      case "$section" in
      versions)
        EXISTING_VERSIONS["$key"]="$value"
        ;;
      commits)
        EXISTING_COMMITS["$key"]="$value"
        ;;
      esac
      ;;
    esac
  done <"$VERSIONS_FILE"
}

load_existing_checksums() {
  local key
  local line
  local value

  [[ -f "$CHECKSUMS_FILE" ]] || return

  while IFS= read -r line; do
    [[ "$line" == '  "'*'": "'*'"' ]] || continue
    key="${line#  \"}"
    key="${key%%\": \"*}"
    value="${line##*\": \"}"
    value="${value%\"}"
    EXISTING_CHECKSUMS["$key"]="$value"
  done <"$CHECKSUMS_FILE"
}

normalize_version() {
  sed -nE 's/^[^0-9]*([0-9]+([.][0-9]+)*)$/\1/p'
}

latest_version_for() {
  local repo="$1"
  local version

  if ! version="$(
    gh release view \
      --repo "$repo" \
      --json tagName \
      --jq '.tagName' 2>/dev/null | normalize_version
  )"; then
    version=""
  fi

  if [[ -z "$version" ]]; then
    if ! version="$(
      gh api \
        --method GET \
        --header 'Accept: application/vnd.github+json' \
        --jq 'if type == "array" then .[0].name // empty else empty end' \
        "https://api.github.com/repos/$repo/tags" | normalize_version
    )"; then
      version=""
    fi
  fi

  if [[ ! "$version" =~ ^[0-9]+([.][0-9]+)*$ ]]; then
    version=""
  fi

  echo "$version"
}

latest_commit_for() {
  local repo="$1"
  local ref="$2"

  gh api \
    --method GET \
    --header 'Accept: application/vnd.github+json' \
    --jq '.sha' \
    "https://api.github.com/repos/$repo/commits/$ref"
}

version_key_for() {
  echo "$1" | cut -d '/' -f 2 | tr -d '-'
}

update_versions() {
  local repo
  local ref
  local key
  local version
  local commit
  local old_commit
  local old_version
  local spec

  for repo in "${repos[@]}"; do
    printf 'processing repo: %s ... ' "$repo"

    key="$(version_key_for "$repo")"
    version="$(latest_version_for "$repo")"

    if [[ -z "$version" ]]; then
      echo "failed"
      echo "error: failed to resolve version for $repo" >&2
      exit 1
    fi

    old_version="${EXISTING_VERSIONS[$key]:-}"
    if [[ -n "${EXISTING_VERSIONS[$key]+x}" && "$old_version" == "$version" ]]; then
      echo "version unchanged ($version)"
    else
      echo "version updated (${old_version:-none} -> $version)"
    fi

    UPDATED_VERSIONS["$key"]="$version"
  done

  for spec in "${commit_repos[@]}"; do
    read -r repo ref key <<<"$spec"
    printf 'processing repo commit: %s ... ' "$repo"

    commit="$(latest_commit_for "$repo" "$ref")"

    if [[ ! "$commit" =~ ^[0-9a-f]{40}$ ]]; then
      echo "failed"
      echo "error: failed to resolve commit for $repo" >&2
      exit 1
    fi

    old_commit="${EXISTING_COMMITS[$key]:-}"
    if [[ -n "${EXISTING_COMMITS[$key]+x}" && "$old_commit" == "$commit" ]]; then
      echo "commit unchanged ($commit)"
    else
      echo "commit updated (${old_commit:-none} -> $commit)"
    fi

    UPDATED_COMMITS["$key"]="$commit"
  done
}

write_versions_file() {
  local output_file="$1"
  local repo
  local ref
  local key
  local spec

  {
    echo "# Auto-generated by ./update-version.sh — DO NOT EDIT"
    echo "# Generated at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")"
    echo
    echo "versions:"
  } >"$output_file"

  for repo in "${repos[@]}"; do
    key="$(version_key_for "$repo")"
    {
      echo "  # https://github.com/$repo"
      echo "  $key: ${UPDATED_VERSIONS[$key]}"
    } >>"$output_file"
  done

  echo >>"$output_file"
  echo "commits:" >>"$output_file"

  for spec in "${commit_repos[@]}"; do
    read -r repo ref key <<<"$spec"
    {
      echo "  # https://github.com/$repo"
      echo "  $key: ${UPDATED_COMMITS[$key]}"
    } >>"$output_file"
  done
}

# Strip the "Generated at:" header line and compare content-only equality.
yaml_content_eq() {
  local a="$1" b="$2"
  [[ -f "$a" && -f "$b" ]] || return 1
  diff <(sed '/^# Generated at:/d' "$a") <(sed '/^# Generated at:/d' "$b") >/dev/null 2>&1
}

safe_mv_if_changed() {
  local src="$1" dst="$2"
  if yaml_content_eq "$src" "$dst"; then
    echo "content unchanged, skipping write: $dst" >&2
    rm -f "$src"
  else
    mv "$src" "$dst"
  fi
}

write_json_object() {
  local map_name="$1"
  local first=1
  local key
  local -n map_ref="$map_name"

  printf '{'
  for key in "${!map_ref[@]}"; do
    if [[ "$first" -eq 0 ]]; then
      printf ','
    fi
    first=0
    printf '"%s":"%s"' "$key" "${map_ref[$key]}"
  done
  printf '}'
}

render_override_data() {
  local os="$1"
  local arch="$2"
  local versions_name="$3"
  local commits_name="$4"

  # chezmoi merges multiple override data sources in surprising ways, so pass
  # target data plus the in-memory version/commit maps as a single JSON object.
  printf '{"chezmoi":{"os":"%s","arch":"%s","fqdnHostname":"update-version"},"versions":' "$os" "$arch"
  write_json_object "$versions_name"
  printf ',"commits":'
  write_json_object "$commits_name"
  printf '}'
}

extract_urls() {
  sed -n -e 's/^[[:space:]]*url:[[:space:]]*"\(.*\)"[[:space:]]*$/\1/p' \
         -e 's/^[[:space:]]*-[[:space:]]*"\(.*\)"[[:space:]]*$/\1/p' \
    | sort -u
}

canonical_key_for_url() {
  local url="$1"

  url="${url#https://}"
  url="${url#cdn.gh-proxy.org/https://}"
  echo "$url"
}

add_rendered_urls() {
  local versions_array_name="$1"
  local commits_array_name="$2"
  local urls_array_name="$3"
  local keys_array_name="${4:-}"
  local arch
  local key
  local os
  local rendered_file
  local target_data
  local target_config
  local target
  local url
  local -n urls_ref="$urls_array_name"

  if [[ -n "$keys_array_name" ]]; then
    local -n keys_ref="$keys_array_name"
  fi

  rendered_file=""

  for target in "${targets[@]}"; do
    os="${target%/*}"
    arch="${target#*/}"
    target_data="$(render_override_data "$os" "$arch" "$versions_array_name" "$commits_array_name")"

    # Render the bootstrap config for each target first; it defines derived
    # fields such as archive suffixes, architecture aliases, and URL prefixes.
    target_config="$(env \
      DOTFILES_EXTRA_BINS=1 \
      DOTFILES_ARKADE_BINS=1 \
      DOTFILES_MIRROR= \
      chezmoi --source "$ROOT_DIR" execute-template \
      --override-data "$target_data" \
      --file "$CONFIG_TEMPLATE")"
    # Render externals using the same target data plus either EXISTING_* or
    # UPDATED_* maps, then collect the concrete URLs that require checksums.
    rendered_file="$(env \
      DOTFILES_EXTRA_BINS=1 \
      DOTFILES_ARKADE_BINS=1 \
      DOTFILES_MIRROR= \
      chezmoi --source "$ROOT_DIR" \
      --config <(printf '%s\n' "$target_config") \
      --config-format yaml \
      execute-template \
      --override-data "$target_data" \
      --file "$EXTERNALS_TEMPLATE")"

    while IFS= read -r url; do
      # shellcheck disable=SC2034
      urls_ref["$url"]=1
      if [[ -n "$keys_array_name" ]]; then
        key="$(canonical_key_for_url "$url")"
        # shellcheck disable=SC2034
        keys_ref["$key"]=1
      fi
    done < <(extract_urls <<<"$rendered_file")
  done
}

write_checksum() {
  local url="$1"
  local reuse_existing="${2:-0}"
  local key
  local sha256

  key="$(canonical_key_for_url "$url")"

  # A checksum is reusable only when the same canonical URL was rendered from
  # both the old and updated version maps. Historical checksum entries alone
  # are not enough, because they may refer to versions no longer requested.
  if [[ "$reuse_existing" -eq 1 && -n "${EXISTING_CHECKSUMS[$key]+x}" ]]; then
    echo "cached checksum: $url" >&2
    printf '  "%s": "%s"\n' "$key" "${EXISTING_CHECKSUMS[$key]}"
    return
  fi

  echo "checksumming: $url" >&2
  if ! sha256="$(curl --fail --silent --show-error --location --retry 3 "$url" | sha256sum | awk '{print $1}')"; then
    echo "error: failed to checksum URL: $url" >&2
    return 1
  fi
  printf '  "%s": "%s"\n' "$key" "$sha256"
}

checksum_output_for_urls() {
  local urls_array_name="$1"
  local reusable_keys_array_name="$2"
  local failed=0
  local key
  local pid
  local pids=()
  local reuse_existing
  local sorted_urls=()
  local url
  local -A seen_keys=()
  # shellcheck disable=SC2178
  local -n urls_ref="$urls_array_name"
  local -n reusable_keys_ref="$reusable_keys_array_name"

  mapfile -t sorted_urls < <(printf '%s\n' "${!urls_ref[@]}" | sort)

  {
    for url in "${sorted_urls[@]}"; do
      key="$(canonical_key_for_url "$url")"
      if [[ -n "${seen_keys[$key]+x}" ]]; then
        continue
      fi
      seen_keys["$key"]=1
      reuse_existing=0
      if [[ -n "${reusable_keys_ref[$key]+x}" ]]; then
        reuse_existing=1
      fi

      write_checksum "$url" "$reuse_existing" &
      pids+=("$!")

      # Track only checksum worker PIDs. wait -n can observe unrelated process
      # substitutions in this script and turn successful runs into false
      # failures, so each batch waits for the exact workers it started.
      if [[ "${#pids[@]}" -ge "$JOBS" ]]; then
        for pid in "${pids[@]}"; do
          if ! wait "$pid"; then
            failed=1
          fi
        done
        pids=()
      fi
    done

    for pid in "${pids[@]}"; do
      if ! wait "$pid"; then
        failed=1
      fi
    done

    if [[ "$failed" -ne 0 ]]; then
      echo "error: failed to checksum one or more URLs" >&2
      exit 1
    fi
  } | sort
}

update_checksums() {
  local key
  local checksum_output
  local line
  local value

  # shellcheck disable=SC2034
  local -A old_commits=()
  # shellcheck disable=SC2034
  local -A old_keys=()
  # shellcheck disable=SC2034
  local -A old_urls=()
  # shellcheck disable=SC2034
  local -A old_versions=()
  local -A urls=()

  # Render old and updated external URL sets. Their key intersection determines
  # which existing checksums can be reused without downloading the asset again.
  if [[ -f "$VERSIONS_FILE" ]]; then
    for key in "${!EXISTING_VERSIONS[@]}"; do
      old_versions["$key"]="${EXISTING_VERSIONS[$key]}"
    done
    for key in "${!EXISTING_COMMITS[@]}"; do
      old_commits["$key"]="${EXISTING_COMMITS[$key]}"
    done
    for key in "${!UPDATED_VERSIONS[@]}"; do
      if [[ -z "${old_versions[$key]+x}" ]]; then
        old_versions["$key"]="${UPDATED_VERSIONS[$key]}"
      fi
    done
    for key in "${!UPDATED_COMMITS[@]}"; do
      if [[ -z "${old_commits[$key]+x}" ]]; then
        old_commits["$key"]="${UPDATED_COMMITS[$key]}"
      fi
    done

    add_rendered_urls old_versions old_commits old_urls old_keys
  fi

  add_rendered_urls UPDATED_VERSIONS UPDATED_COMMITS urls

  if ((${#urls[@]} == 0)); then
    return
  fi

  if ! checksum_output="$(checksum_output_for_urls urls old_keys)"; then
    return 1
  fi

  while IFS= read -r line; do
    [[ "$line" == '  "'*'": "'*'"' ]] || continue
    key="${line#  \"}"
    key="${key%%\": \"*}"
    value="${line##*\": \"}"
    value="${value%\"}"
    UPDATED_CHECKSUMS["$key"]="$value"
  done <<<"$checksum_output"
}

write_checksums_file() {
  local output_file="$1"
  local key

  {
    echo "# Auto-generated by ./update-version.sh — DO NOT EDIT"
    echo "# Generated at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")"
    echo
    echo "checksums:"
  } >"$output_file"
  if ((${#UPDATED_CHECKSUMS[@]} == 0)); then
    return
  fi

  while IFS= read -r key; do
    printf '  "%s": "%s"\n' "$key" "${UPDATED_CHECKSUMS[$key]}"
  done < <(printf '%s\n' "${!UPDATED_CHECKSUMS[@]}" | sort) >>"$output_file"
}

cleanup_temp_dir() {
  if [[ -n "$UPDATE_VERSION_TEMP_DIR" ]]; then
    rm -rf "$UPDATE_VERSION_TEMP_DIR"
  fi
}

main() {
  local temp_checksums_file
  local temp_versions_file

  if [[ ! "$JOBS" =~ ^[1-9][0-9]*$ ]]; then
    echo "error: UPDATE_VERSION_JOBS must be a positive integer: $JOBS" >&2
    exit 1
  fi

  require_command chezmoi
  require_command curl
  require_command gh
  require_command sha256sum

  load_existing_versions
  load_existing_checksums

  if [[ "$SKIP_VERSIONS" -eq 1 && "$SKIP_CHECKSUMS" -eq 1 && (! -f "$VERSIONS_FILE" || ! -f "$CHECKSUMS_FILE") ]]; then
    echo "error: --skip-versions and --skip-checksums both set, but the required files do not both exist" >&2
    exit 1
  fi

  if [[ "$SKIP_VERSIONS" -eq 1 ]]; then
    for key in "${!EXISTING_VERSIONS[@]}"; do
      UPDATED_VERSIONS["$key"]="${EXISTING_VERSIONS[$key]}"
    done
    for key in "${!EXISTING_COMMITS[@]}"; do
      UPDATED_COMMITS["$key"]="${EXISTING_COMMITS[$key]}"
    done
  else
    update_versions
  fi

  if [[ "$SKIP_CHECKSUMS" -eq 1 ]]; then
    for key in "${!EXISTING_CHECKSUMS[@]}"; do
      UPDATED_CHECKSUMS["$key"]="${EXISTING_CHECKSUMS[$key]}"
    done
  else
    update_checksums
  fi

  UPDATE_VERSION_TEMP_DIR="$(mktemp -d "$ROOT_DIR/.update-version.XXXXXX")"
  trap cleanup_temp_dir EXIT

  if [[ "$SKIP_VERSIONS" -eq 0 ]]; then
    temp_versions_file="$UPDATE_VERSION_TEMP_DIR/versions.yaml"
    write_versions_file "$temp_versions_file"
    safe_mv_if_changed "$temp_versions_file" "$VERSIONS_FILE"
  fi

  if [[ "$SKIP_CHECKSUMS" -eq 0 ]]; then
    temp_checksums_file="$UPDATE_VERSION_TEMP_DIR/checksums.yaml"
    write_checksums_file "$temp_checksums_file"
    safe_mv_if_changed "$temp_checksums_file" "$CHECKSUMS_FILE"
  fi

  cleanup_temp_dir
  UPDATE_VERSION_TEMP_DIR=""
  trap - EXIT
}

main
